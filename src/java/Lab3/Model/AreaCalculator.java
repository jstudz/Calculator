
package Lab3.Model;

import Lab2.Model.*;
import Lab1.Model.*;


public class AreaCalculator {
    
    public double calculateRectangle(double length, double width) {
        double area = 0;
        
        return area = length * width;
    }
    
    public double calculateTriangle (double height, double base) {
        double area = 0;
        
        return area = (height * base) / 2;
    }
    
    public double calculateCircle (double radius) {
        double area = 0;
        
        return area = Math.PI * Math.pow(radius, 2);
        
    }
}
